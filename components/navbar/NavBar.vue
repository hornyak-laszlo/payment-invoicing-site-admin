<template>
  <nav v-show="isNavBarVisible" id="navbar-main" class="navbar is-fixed-top">
    <div class="navbar-brand">
      <a
        class="navbar-item is-hidden-desktop"
        @click.prevent="menuToggleMobile"
      >
        <b-icon :icon="menuToggleMobileIcon" pack="fas" />
      </a>
      <!--div class="navbar-item no-left-space-touch">
        <div class="control">
          <input class="input" placeholder="Search everywhere...">
        </div>
      </div-->
    </div>
    <div class="navbar-brand is-right">
      <a
        class="navbar-item navbar-item-menu-toggle is-hidden-desktop"
        @click.prevent="menuNavBarToggle"
      >
        <b-icon :icon="menuNavBarToggleIcon" pack="fas" custom-size="default" />
      </a>
    </div>
    <div
      class="navbar-menu fadeIn animated faster"
      :class="{ 'is-active': isMenuNavBarActive }"
    >
      <div class="navbar-end">
        <nav-bar-menu class="has-divider has-user-avatar">
          <user-avatar />
          <div class="is-user-name">
            <span>{{ username }}</span>
          </div>

          <div slot="dropdown" class="navbar-dropdown">
            <nuxt-link
              to="/profile"
              class="navbar-item"
              exact-active-class="is-active"
            >
              <b-icon pack="fas" icon="account" custom-size="default" />
              <span>Profil</span>
            </nuxt-link>
            <hr class="navbar-divider">
            <a class="navbar-item" @click="logout">
              <b-icon pack="fas" icon="logout" custom-size="default" />
              <span>Kijelentkezés</span>
            </a>
          </div>
        </nav-bar-menu>
        <a
          class="navbar-item has-divider is-desktop-icon-only"
          title="Dark mode"
          @click="darkModeToggle"
        >
          <b-icon :icon="darkModeToggleIcon" pack="fas" custom-size="default" />
          <span>Dark mode</span>
        </a>
        <a
          class="navbar-item is-desktop-icon-only"
          title="Kijelentkezés"
          @click="logout"
        >
          <b-icon pack="fas" icon="logout" custom-size="default" />
          <span>Kijelentkezés</span>
        </a>
      </div>
    </div>
  </nav>
</template>

<script>
import { mapState } from 'vuex'
import NavBarMenu from '@/components/navbar/NavBarMenu'
import UserAvatar from '@/components/UserAvatar'

export default {
  name: 'NavBar',
  components: {
    UserAvatar,
    NavBarMenu
  },
  data () {
    return {
      isMenuNavBarActive: false
    }
  },
  computed: {
    username () {
      return this.$strapi.user ? this.$strapi.user.username : ''
    },
    menuNavBarToggleIcon () {
      return this.isMenuNavBarActive ? 'close' : 'dots-vertical'
    },
    menuToggleMobileIcon () {
      return this.isAsideMobileExpanded ? 'backburger' : 'forwardburger'
    },
    darkModeToggleIcon () {
      return this.isDarkModeActive ? 'white-balance-sunny' : 'weather-night'
    },
    ...mapState([
      'isNavBarVisible',
      'isAsideMobileExpanded',
      'isDarkModeActive'
    ])
  },
  methods: {
    menuToggleMobile () {
      this.$store.commit('asideMobileStateToggle')
    },
    menuNavBarToggle () {
      this.isMenuNavBarActive = !this.isMenuNavBarActive
    },
    darkModeToggle () {
      this.$store.commit('darkModeToggle')
    },
    async logout () {
      try {
        await this.$strapi.logout()
        this.$router.push('/login')
      } catch (err) {
        this.$buefy.snackbar.open({
          message: 'Kijelentkezés sikertelen',
          type: 'is-danger',
          queue: false
        })
      }
    }
  }
}
</script>
